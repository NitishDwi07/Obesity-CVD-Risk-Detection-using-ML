    public void showTableData() {
        try {
            conn = DatabaseConnection.getConnection();
            sql = "SELECT id AS 'No.', med_name AS 'Medicine Name', manufacturer AS 'Manufacturer Name', quantity AS 'Quantity', price_per_unit AS 'Price Per Unit' FROM medicineinventory";
            stmt = conn.prepareStatement(sql);
            result = stmt.executeQuery();
            jTblMedInventory.setModel(DbUtils.resultSetToTableModel(result));
        } catch (SQLException | HeadlessException ex) {
            JOptionPane.showMessageDialog(null, ex);
        } finally {
            try {
                result.close();
                stmt.close();
            } catch (SQLException | HeadlessException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }

        updateTableData();

    }

