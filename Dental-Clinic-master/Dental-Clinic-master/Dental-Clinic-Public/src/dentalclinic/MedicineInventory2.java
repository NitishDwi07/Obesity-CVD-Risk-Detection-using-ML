    private void jTblMedInventoryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTblMedInventoryMouseClicked
        // TODO add your handling code here:
         try
        {
           int r = jTblMedInventory.getSelectedRow();
           String rowClick = (jTblMedInventory.getModel().getValueAt(r,0).toString());
           String sql1= "select * from medicineinventory where id='"+rowClick+"'";
           stmt=conn.prepareStatement(sql1);
           result = stmt.executeQuery();
           
           if (result.next())
           {
               jtfId.setText(result.getString("id")) ;
               jtfMedName.setText(result.getString("med_name")) ;
               jtfManu.setText(result.getString("manufacturer")) ;
               jtfQty.setText(result.getString("quantity")) ;
               jtfPrice.setText(result.getString("price_per_unit")) ;
           }
        }
        catch(Exception ex)
        {
        JOptionPane.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_jTblMedInventoryMouseClicked

